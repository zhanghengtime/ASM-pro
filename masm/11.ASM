DATA SEGMENT
    TIP DB 'please input a word: $' ;提示
	COL DB 0H ;列
	LINE DB 2H ;行
	W DB ? ;字
DATA ENDS

STACK1 SEGMENT STACK
	DW 20H DUP(0)
STACK1 ENDS

CODE SEGMENT
	ASSUME CS:CODE,SS:STACK1,DS:DATA
START:MOV AX,DATA
	  MOV DS,AX
	  LEA DX,TIP  ;输出提示
	  MOV AH,09H
	  INT 21H
	  MOV AH,01H
	  INT 21H
	  MOV W,AL
	  MOV CX,25
	  
 LOP1:PUSH CX  ;下落
 	  CALL LO
 	  POP CX
 	  MOV AH,02H
 	  MOV BH,00H
 	  MOV DH,COL
 	  MOV DL,LINE
 	  INT 10H
 	  PUSH CX
 	  CALL LOWER
 	  POP CX
 	  INC COL
 	  LOOP LOP1
 	  MOV AH,4CH
 	  INT 21H

LO PROC  ;清屏
	MOV AH,6
	MOV AL,0
	MOV BH,07
	MOV CX,0
	MOV DH,24
	MOV DL,79
	INT 10H
	RET
LO ENDP

LOWER PROC  ;写字符
	MOV AH,09H
	MOV AL,W
	MOV BH,0
	MOV BL,3
	MOV CX,1
	INT 10H
	MOV BX,0FFFFH
L1: DEC BX
	JNE L1
	MOV BX,0FFFFH
L2: DEC BX
	JNE L2	
	RET
LOWER ENDP
	
CODE ENDS
	END START	  

