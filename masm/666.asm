;编写程序将屏幕分割成格状，每一格涂上不同颜色，格数从键盘输入

DATA SEGMENT
	TIP DB 'please input number: $'
	COL DW 1
	ROW DW 1
	COLOR DB 1
	NUMBER DB ?
	LEN DW 320
DATA ENDS

STACK1 SEGMENT STACK
	DW 20H DUP(0)
STACK1 ENDS

CODE SEGMENT 
	ASSUME CS:CODE,DS:DATA,SS:STACK1
START:	MOV AX,DATA
		MOV DS,AX
		LEA DX,TIP
		MOV AH,09H
		INT 21H
		MOV AH,01H
		INT 21H
		SUB AL,30H
		MOV NUMBER,AL
		MOV AX,9
		MOV BL,NUMBER
		DIV BL
		MOV NUMBER,AL
		MOV AH,00H
		MOV AL,0DH
		INT 10H
		MOV CL,NUMBER
LOP2:	PUSH CX
		CALL WRITE
		;MOV AL,NUMBER
		;CMP COL,AX
		;JZ L1 
		INC COL
		INC COLOR
		POP CX
		LOOP LOP2
L1:		MOV AH,0
        INT 21H
        MOV AH,4CH
		INT 21H
		
WRITE PROC
        MOV CX,1
		MOV DX,1
		MOV BP,20
LOP1:	MOV BH,0
		INC DX
		MOV AL,COLOR
		MOV AH,0CH
		INT 10H
		DEC BP
		JNE LOP1
		RET
WRITE ENDP
CODE ENDS
	END START